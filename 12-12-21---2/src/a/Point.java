package a;

public class Point {

		int x = 5; 
		int t = 9;
		public Point(int x, int t) {
			super();
			this.x = x;
			this.t = t;
		}
		public int getX() {
			return x;
		}
		public void setX(int x) {
			this.x = x;
		}
		public int getT() {
			return t;
		}
		public void setT(int t) {
			this.t = t;
		}
		
}
